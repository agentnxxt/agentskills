# ─── HELPERS ──────────────────────────────────────────────────────────────────

def slugify(name: str) -> str:
    import re
    return re.sub(r"[^a-z0-9]+", "-", name.lower()).strip("-")

def state_path(slug: str) -> Path:
    return DATA_DIR / f"{slug}.json"

def load_state(slug: str) -> dict:
    p = state_path(slug)
    if p.exists():
        return json.loads(p.read_text())
    return {}

def save_state(slug: str, state: dict):
    state_path(slug).write_text(json.dumps(state, indent=2))

def calc_u_index(scores: dict) -> float:
    total = 0.0
    for stage in STAGES:
        stage_metrics = [m for m in METRICS if m["stage"] == stage["id"]]
        scored = [m for m in stage_metrics if scores.get(m["id"]) is not None]
        if not scored:
            continue
        stage_sum = sum(scores[m["id"]] for m in scored)
        stage_score = (stage_sum / (len(stage_metrics) * 5)) * 5
        total += stage_score * stage["weight"]
    return round(total, 2)

def get_verdict(score: float) -> dict:
    for v in VERDICT_THRESHOLDS:
        if score >= v["min"]:
            return v
    return VERDICT_THRESHOLDS[-1]

def get_blockers(scores: dict) -> list:
    return [
        m for m in METRICS
        if m["stage"] in (2, 4, 5)
        and scores.get(m["id"]) is not None
        and scores[m["id"]] <= 1
    ]

def claude_score(product: str, use_case: str, batch: list) -> dict:
    """Call Claude API to score a batch of metrics."""
    if not ANTHROPIC_API_KEY:
        # Fallback: return neutral scores if no API key
        return {"scores": {m["id"]: 3 for m in batch}, "evidence": {m["id"]: "Fallback score — set ANTHROPIC_API_KEY for AI research" for m in batch}}
    
    prompt_lines = "\n".join(f"{m['id']}: {m['name']}" for m in batch)
    system = (
        f'You are a SaaS enterprise evaluator. Score the following metrics for "{product}" '
        f'(use case: {use_case or "general enterprise"}). '
        'Return ONLY valid JSON: {"scores":{"metric_id": score_0_to_5}, "evidence":{"metric_id": "brief evidence"}} '
        'Score 0-5 integers. Be realistic and conservative. No extra text.'
    )
    resp = httpx.post(
        "https://api.anthropic.com/v1/messages",
        headers={
            "x-api-key": ANTHROPIC_API_KEY,
            "anthropic-version": "2023-06-01",
            "content-type": "application/json",
        },
        json={
            "model": "claude-sonnet-4-20250514",
            "max_tokens": 1000,
            "system": system,
            "messages": [{"role": "user", "content": prompt_lines}],
        },
        timeout=60,
    )
    resp.raise_for_status()
    text = resp.json()["content"][0]["text"]
    clean = text.replace("```json", "").replace("```", "").strip()
    parsed = json.loads(clean)
    return parsed

